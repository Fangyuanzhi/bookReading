def pad_chapter_content(slug: str, title: str, content: str) -> str:
    """Extend chapter with thematic paragraphs until MIN_CHARS (cap at MAX_CHARS)."""
    if "\n\n" not in content:
        return content
    _, body = content.split("\n\n", 1)
    paragraphs = [p for p in body.split("\n\n") if p.strip()]
    bank = PAD_BANKS.get(slug, [])
    bank_idx = 0
    while char_count(join_paragraphs(title, paragraphs)) < MIN_CHARS and bank:
        paragraphs.append(bank[bank_idx % len(bank)])
        bank_idx += 1
    while char_count(join_paragraphs(title, paragraphs)) > MAX_CHARS and len(paragraphs) > 3:
        paragraphs.pop()
    return join_paragraphs(title, paragraphs)


