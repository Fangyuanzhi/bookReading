def char_count(text: str) -> int:
    return len(text.replace("\n", "").replace(" ", ""))


def join_paragraphs(title: str, paragraphs: list[str]) -> str:
    body = "\n\n".join(p.strip() for p in paragraphs if p.strip())
    return f"{title}\n\n{body}"


